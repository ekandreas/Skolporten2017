            <h3>Varför blev du intresserad av ämnet?</h3>
            <p>– Jag är folkhälsovetare och har tidigare arbetat som folkhälsoplanerare inom landstinget. Där kom jag i kontakt med Disa, en metod som används i skolan för att minskar depressiva symtom hos unga. Disa påstods vara evidensbaserad men vid en närmare granskning såg jag att evidensen bestod av en magisteruppsats. När jag fick möjlighet att forska ville jag undersöka effekten av Disa men också vilka faktorer som ligger bakom depressiva symptom bland ungdomar.</p>

            <h3>Vad handlar avhandlingen om?</h3>
            <p>– Om vilken effekt Disa har på de ungdomar som erbjuds metoden. Men också vilka individuella, psykosociala och strukturella faktorer som kan ha betydelse för depressiva symptom bland unga. Avhandlingen bygger på enkäter bland drygt 1000 elever mellan 14-16 år i en kommun. Enkäterna gjordes vid tre tillfällen med ett års mellanrum. Eleverna har fått svara på frågor om självkänsla, relationer med föräldrar, kompisar, lärare, sexuella trakasserier, mobbning, invandrarbakgrund och materiellt välstånd. De har också fått svara på om de gått Disa-programmet under året.</p>

            <h3>Vilka är de viktigaste resultaten?</h3>
            <p>– Att sexuella trakasserier är en stark faktor bakom depressiva symptom bland tjejer i den här åldern. Med sexuella trakasserier menas här att mot sin vilja bli utsatt för glåpord, ryktesspridning, tafsad på, få bilder med sexuellt innehåll skickade till sig, bli offentlig kommenterad för sin egen kropp men också rena övergrepp.</p>
            <p>– Resultaten visar att tjejer och killar i lika hög grad är utsatta för sexuella trakasserier. Men det är nästan bara är för tjejer som detta har samband med depressiva symptom. Verbala trakasserier med homofobiskt innehåll är vanligare bland killar medan tjejerna även trakasseras fysiskt eller genom offentlig kränkning. Den som trakasserar är i de allra flesta fall en kille i samma skola, därefter en kille i en annan skola, pojkvän, en kille som de inte känner och i sista hand en vuxen. Även tjejer trakasserar, tidigare forskning visar att det då ofta handlar om att ”ge igen”. De killar som utsätts för trakasserier mår ofta redan dåligt. Den viktigaste bestämningsfaktorn för depressiva symptom bland pojkar är att inte ha lika mycket pengar som kompisarna.</p>
            <p>– Min analys är feministiskt – jag menar att skillnaderna i resultat när det gäller sexuella trakasserier mellan tjejer och killar förklaras av att sexuella trakasserier är ett verktyg för att skapa en viss maktordning där den överordnade maskuliniteten dominerar. Tjejer som blir utsatta för sexuella trakasserier förkroppsligar den värld de lever i. De killar som mår dåligt blir utsatta för att de inte är tillräckligt maskulina utifrån det idealiserade sätt att vara kille på.</p>
            <p>– De elever som erbjudits Disa mellan enkätundersökningarna mådde varken bättre eller sämre efteråt. Insatsen gav helt enkelt inte någon effekt. Min tolkning av detta är att Disa erbjuds till de elever som mår sämst. Men Disa är en förebyggande metod, inte någon behandling. Mycket tyder på att skolorna erbjuder Disa till fel målgrupp och att det är därför som metoden inte visar på några positiva effekter.</p>

            <h3>Vad överraskade dig?</h3>
            <p>– Att sexuella trakasserier är en så stark faktor bakom depressiva symptom.</p>

            <h3>Vem har nytta av dina resultat?</h3>
            <p>– Skolorna förstås. När jag pratar med lärare om sexuella trakasserier säger många att, ”nej det är inte utbrett på vår skola”. Men när jag definierar vad jag menar med sexuella trakasserier, det vill säga verbala kränkningar, dra i underkläder och liknande, ja då erkänner de flesta att det är vanligt förekommande. Jag tror att skolorna måste prata mer om vad sexuella trakasserar faktiskt är.</p>

