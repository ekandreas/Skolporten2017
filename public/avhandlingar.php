            <h3>Abstract in English:</h3>
            <p>This dissertation deals with the didactic consequences of assessment for learning (AfL) in the subject of physical education and health (PEH) at three upper secondary schools in Sweden. The purpose of the study is to investigate how assessment for learning is realised in PEH and what triadic relations between the teacher, student and subject content are established in the formative assessment practice. The empirical material consists of group reflections within a Teacher Learning Community (TLC) as well as field studies, including lesson observations and semi structured interviews with both students and teachers. In the first step of the analysis the material is categorized by means of the five key strategies (Wiliam, 2010a), in order to identify different ways of working with AfL in upper secondary PEH. The second step is a combination of a governmentality (Foucault, 1978/1991b), a performativity (Ball, 2003) and a didactic (Hudson, 2002) analysis, which illuminates what triadic relations are established under different conditions of governance.</p>

            <p>The findings highlight five fabrications of AfL in PEH, named after their most prominent features or functions, AfL as: i) Empowerment, ii) Physical Activation, iii) Grade Generation, iv) Constructive Alignment, v) Negotiation. ”Among the products of discursive practices are the very persons who engage in them” (Davies & Harré, 2001, p. 263). Accordingly, different teacher and student subjects as well as characteristics of the subject content are constituted in each of these fabrications.</p>

            <p>Moreover, the so called ‘backwash effect’ (Torrance, 2012) implies that the contrasting versions of AfL promote different kinds of learning, such as: i) increased autonomy, ii) participation in a community of practice, iii) criteria compliance, iv) acquisition of prescribed abilities, v) group development. However, the big idea of AfL is to adapt the teaching to the students and not the students to the knowledge requirements. Hence, this dissertation could serve as a basis for discussion on possible didactic implications of AfL in PEH.</p>
